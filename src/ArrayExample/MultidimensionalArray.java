package ArrayExample;

public class MultidimensionalArray {
    public static void main(String[] args) {
        
        int [][] Matrix = {{1,2,5}, {2,4,6},{4,3,6}};
       
        System.out.println("Element at row 1 and column 0 is:" + Matrix[1][0]);
        System.out.println("Element at row 1 and column 1 is:" + Matrix [1][1]);
        System.out.println("Element at row 2 and column 2 is:" + Matrix[2][2]);
        System.out.println("Element at row 1 and column 2 is:" + Matrix[1][2]);
    }
    
}
